public class Equals {
    public static void main(String[] args) {
        String name_1 = "João";
        String namte_2 = "Mara";
        boolean comparison = name_1.equals(namte_2);

        if (comparison) {
            System.out.println("São iguais.");
        } else {
            System.out.println("São diferentes.");
        }
    }
}
