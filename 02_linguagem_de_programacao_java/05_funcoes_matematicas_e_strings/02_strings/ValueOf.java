public class ValueOf {
    public static void main(String[] args) {
        int number_1 = 17;
        float number_2 = (float) 28.9;
        String new_string_1 = String.valueOf(number_1);
        String new_string_2 = String.valueOf(number_2);
        System.out.println(new_string_1);
        System.out.println(new_string_2);
    }
}
