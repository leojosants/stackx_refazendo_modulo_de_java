public class App {
    public static void main(String[] args) throws Exception {
        // ++ --

        int number = 10;

        System.out.println(number++); // 10 + 1 = 11
        System.out.println(++number); // 1 + 11 = 12
        System.out.println(number--); // 12 - 1 = 11
        System.out.println(--number); // 1 - 11 = 10
    }
}
