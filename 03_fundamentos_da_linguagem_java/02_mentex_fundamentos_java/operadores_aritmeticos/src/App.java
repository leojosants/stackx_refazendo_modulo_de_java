public class App {
    public static void main(String[] args) throws Exception {
        /*
         * operators: + - * / %
         * order of precedence: * / % + -
         */

        System.out.println(1 + 3);
        System.out.println(5 - 3);
        System.out.println(5 * 3);
        System.out.println(5 / 3);
        System.out.println(5 % 3);
        System.out.println(10 * 10 / 5 + 3 - 4 / 2);
    }
}
